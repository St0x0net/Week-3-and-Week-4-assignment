import java.util.Arrays;

public class Problem3 {
    // Merge Sort to handle large datasets with O(n log n) stability
    public static void mergeSort(int[] arr, int left, int right) {
        if (left < right) {
            int mid = left + (right - left) / 2;
            mergeSort(arr, left, mid);
            mergeSort(arr, mid + 1, right);
            merge(arr, left, mid, right);
        }
    }

    private static void merge(int[] arr, int l, int m, int r) {
        int n1 = m - l + 1;
        int n2 = r - m;
        int[] L = new int[n1];
        int[] R = new int[n2];

        for (int i = 0; i < n1; ++i) L[i] = arr[l + i];
        for (int j = 0; j < n2; ++j) R[j] = arr[m + 1 + j];

        int i = 0, j = 0, k = l;
        while (i < n1 && j < n2) {
            // Sorting in Descending Order for volume trends
            if (L[i] >= R[j]) arr[k++] = L[i++];
            else arr[k++] = R[j++];
        }
        while (i < n1) arr[k++] = L[i++];
        while (j < n2) arr[k++] = R[j++];
    }

    public static void main(String[] args) {
        int[] volumes = {1200, 4500, 800, 3200, 5000};
        System.out.println("Original Volumes: " + Arrays.toString(volumes));
        mergeSort(volumes, 0, volumes.length - 1);
        System.out.println("Sorted Volumes (Desc): " + Arrays.toString(volumes));
    }
}